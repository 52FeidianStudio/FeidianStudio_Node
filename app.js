var express = require('express');
var path = require('path');
var favicon = require('serve-favicon');
var logger = require('morgan');
var cookieParser = require('cookie-parser');
var bodyParser = require('body-parser');

var welcome = require('./routes/welcome');
var index = require('./routes/index');
var introduction = require('./routes/introduction');
var culture = require('./routes/culture');
var members = require('./routes/members');
var news = require('./routes/news');
var works = require('./routes/works');
var recruit = require('./routes/recruit');
var contact = require('./routes/contact');

var dbURL = 'mongodb://localhost/feidian';
var db = require('mongoose').connect(dbURL);

var app = express();

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'ejs');

// uncomment after placing your favicon in /public
//app.use(favicon(path.join(__dirname, 'public', 'favicon.ico')));
app.use(logger('dev'));
app.use(bodyParser.json());
app.use(bodyParser.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

app.use('/', welcome);
app.use('/welcome', welcome);
app.use('/index', index);
app.use('/introduction', introduction);
app.use('/culture', culture);
app.use('/members', members);
app.use('/news', news);
app.use('/works', works);
app.use('/recruit', recruit);
app.use('/contact', contact);

// catch 404 and forward to error handler
app.use(function(req, res, next) {
  var err = new Error('Not Found');
  err.status = 404;
  next(err);
});

// error handlers

// development error handler
// will print stacktrace
if (app.get('env') === 'development') {
  app.use(function(err, req, res, next) {
    res.status(err.status || 500);
    res.render('error', {
      message: err.message,
      error: err
    });
  });
}

// production error handler
// no stacktraces leaked to user
app.use(function(err, req, res, next) {
  res.status(err.status || 500);
  res.render('error', {
    message: err.message,
    error: {}
  });
});


module.exports = app;
